#list of car i love
cars = ['Porsche', 'Mercedes', 'landrover', 'audi', 'Mini Cooper', 'volvo']
print(cars)

#Original list
print("\n\tHere is the original list: ")
print(cars)

#sort for once
print("\n\tHere is the Sorted list: ")
print(sorted(cars))

#Original list again
print("\n\tHere is the original list again: ")
print(cars)

#Sort my cars
print("\n\tHere is the Permanent sorted list: ")
cars.sort()
print(cars)

#reverse sort my cars
print("\n\tHere is the Permanent reverse sorted list: ")
cars.sort(reverse=True)
print(cars)


